<?php

namespace App\Services;

use Illuminate\Support\Carbon;

/**
 * Режим «совещание» в ИИ-чате: повестка, вопросы, список задач → внесение в CRM.
 */
class MeetingAiService
{
    /** Дополнение к активному промпту: роль модератора совещания. */
    public static function facilitatorInstructions(): string
    {
        return <<<'TXT'
Ты ведёшь деловое совещание в CRM. Опирайся на описание организации и контекст из активного system prompt выше и на блоки CONTEXT (данные CRM), если они переданы.

Если в CONTEXT передано поле `company_events` — это зафиксированные события компании (важные факты, влияющие на общую картину). Учитывай их при повестке, вопросах и итоговых задачах.

Порядок работы:
1) Кратко поздоровайся и предложи ПОВЕСТКУ совещания (пункты 3–7), исходя из цели встречи и контекста.
2) Задай уточняющие вопросы по повестке (по одному-два за раз, не перегружай).
3) По итогам обсуждения сформируй ИТОГОВЫЙ СПИСОК ЗАДАЧ нумерованным списком. Для каждой задачи укажи: формулировку, ответственного (ФИО из справочника сотрудников ниже — только из этого списка), срок (дата), статус на доске CRM (один из: «В разработке», «Обработка», «Исполнение», «Завершено»), комментарий/пояснение к задаче.
4) В конце явно попроси пользователя проверить список и при необходимости поправить формулировки в чате.
5) Напомни: когда всё согласовано, нужно нажать кнопку «Создать задачи в CRM» под чатом или написать фразу «подтверждаю создание задач в CRM».

Не выдумывай сотрудников вне переданного JSON-справочника. Если срок не обсуждали — предложи разумный или оставь «уточнить». Будь краток, деловой тон, умеренный юмор допустим. Без markdown-таблиц.
TXT;
    }

    /** Приветствие при создании диалога совещания (без вызова ИИ). */
    public static function welcomeMessage(Carbon $meetingAt): string
    {
        $when = $meetingAt->format('d.m.Y H:i');

        return <<<TXT
Режим совещания. Дата и время для протокола: {$when}.

Я опираюсь на активный промпт организации и (если включено в настройках) снимок данных CRM в контексте.

Опишите цель встречи или напишите «начать» — предложу повестку и вопросы.
TXT;
    }
}
